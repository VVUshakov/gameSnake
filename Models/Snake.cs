namespace gameSnake.Models
{
    /// <summary>
    /// ???????????? ?????? ?? ??????? ????.
    /// ???????? ?????? ????????? ???? ? ?????? ??? ?????? ? ???????.
    /// </summary>
    public class Snake
    {
        /// <summary>
        /// ?????? ???? ????????? ??????.
        /// Body[0] Ś ?????, Body[Body.Count - 1] Ś ??????
        /// </summary>
        public List<Point> Body { get; set; }

        /// <summary>
        /// ?????????? ?????? ?????? (?????? ????????? ??????? ??????)
        /// </summary>
        public Point Head => Body[Body.Count - 1];

        /// <summary>
        /// ?????????? ?????? ?????? (?????? ?????? ??????? ??????)
        /// </summary>
        public Point Tail => Body[0];

        /// <summary>
        /// ?????????????? ????? ?????? ? ????????? ???????? ?????? ? ????????????.
        /// </summary>
        /// <param name="headPosition">??????? ?????? ??????</param>
        /// <param name="direction">????????? ??????????? ????????</param>
        /// <param name="snakeLength">????? ?????? (?? ????????? 3)</param>
        /// <exception cref="ArgumentException">????????????? ??? ??????????? ???????????</exception>
        public Snake(
            Point headPosition,
            Direction direction,
            int snakeLength = 3
        )
        {
            if (snakeLength < 1) snakeLength = 1;

            Body = new List<Point>();

            for (int i = snakeLength - 1; i >= 0; i--)
            {
                switch (direction)
                {
                    case Direction.Right:
                        Body.Add(new Point(headPosition.X - i, headPosition.Y));
                        break;

                    case Direction.Left:
                        Body.Add(new Point(headPosition.X + i, headPosition.Y));
                        break;

                    case Direction.Up:
                        Body.Add(new Point(headPosition.X, headPosition.Y + i));
                        break;

                    case Direction.Down:
                        Body.Add(new Point(headPosition.X, headPosition.Y - i));
                        break;

                    default:
                        throw new ArgumentException($"??????????? ???????????: {direction}");
                }
            }
        }

        /// <summary>
        /// ?????????, ???????? ?? ?????? ????????? ????? (??????? ????).
        /// </summary>
        /// <param name="point">????? ??? ????????</param>
        /// <returns>true, ???? ????? ???????? ?????? ??????; false ? ????????? ??????</returns>
        public bool Contains(Point point)
        {
            foreach (Point segment in Body)
            {
                if (segment.X == point.X && segment.Y == point.Y)
                    return true;
            }
            return false;
        }
    }
}
